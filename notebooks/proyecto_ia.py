import time
from tqdm import tqdm

def train_gan(gan, dataset, epochs, callback):

    # Entrena la GAN por 200 épocas.

    start_time = time.time()

    for epoch in range(epochs):
        epoch_start = time.time()

        gen_losses = []
        disc_losses = []

        # Barra de progreso para cada época
        progress_bar = tqdm(dataset, desc=f"Época {epoch + 1}/{epochs}", leave=False)

        for batch in progress_bar:
            gen_loss, disc_loss = gan.train_step(batch)
            gen_losses.append(gen_loss.numpy())
            disc_losses.append(disc_loss.numpy())

            # Actualizar barra de progreso
            progress_bar.set_postfix({
                'G_loss': f'{gen_loss:.4f}',
                'D_loss': f'{disc_loss:.4f}'
            })

        # Promedios de la época
        avg_gen_loss = np.mean(gen_losses)
        avg_disc_loss = np.mean(disc_losses)
        epoch_time = time.time() - epoch_start

        # Callback
        callback.on_epoch_end(epoch, avg_gen_loss, avg_disc_loss)

        # Imprimir resumen de la época
        print(f"Época {epoch + 1:3d}/{epochs} | "
              f"G_loss: {avg_gen_loss:.4f} | "
              f"D_loss: {avg_disc_loss:.4f} | "
              f"Tiempo: {epoch_time:.1f}s")

        # Guardar cada 50 épocas
        if (epoch + 1) % 50 == 0:
            checkpoint_path = os.path.join(MODELS_PATH, f'checkpoint_epoch_{epoch + 1}')
            gan.generator.save(checkpoint_path + '_generator.keras')
            gan.discriminator.save(checkpoint_path + '_discriminator.keras')
            print(f"    Checkpoint guardado en época {epoch + 1}")

    total_time = time.time() - start_time
    print(f"\n ENTRENAMIENTO COMPLETADO")
    print(f" Tiempo total: {total_time/60:.1f} minutos")

    # Guardar historial
    callback.save_history()

    return callback.history

