# Definición de la Arquitectura DCGAN

from tensorflow.keras import layers, Model
import tensorflow as tf

class DCGAN:

    def __init__(self, latent_dim=100, img_shape=(64, 64, 3)):
        self.latent_dim = latent_dim
        self.img_shape = img_shape

        # Construir modelos
        self.generator = self.build_generator()
        self.discriminator = self.build_discriminator()

        # Optimizadores
        self.gen_optimizer = tf.keras.optimizers.Adam(learning_rate=0.0002, beta_1=0.5)
        self.disc_optimizer = tf.keras.optimizers.Adam(learning_rate=0.0002, beta_1=0.5)

        # Loss
        self.cross_entropy = tf.keras.losses.BinaryCrossentropy(from_logits=True)

    def build_generator(self):
        # Generador: Transforma ruido aleatorio en imágenes de MRI.

        model = tf.keras.Sequential(name='Generator')

        # Capa inicial: Dense para crear feature maps
        model.add(layers.Dense(4 * 4 * 512, use_bias=False, input_shape=(self.latent_dim,)))
        model.add(layers.BatchNormalization())
        model.add(layers.LeakyReLU(0.2))
        model.add(layers.Reshape((4, 4, 512)))

        # Bloque 1: 4x4 -> 8x8
        model.add(layers.Conv2DTranspose(256, (5, 5), strides=(2, 2), padding='same', use_bias=False))
        model.add(layers.BatchNormalization())
        model.add(layers.LeakyReLU(0.2))

        # Bloque 2: 8x8 -> 16x16
        model.add(layers.Conv2DTranspose(128, (5, 5), strides=(2, 2), padding='same', use_bias=False))
        model.add(layers.BatchNormalization())
        model.add(layers.LeakyReLU(0.2))

        # Bloque 3: 16x16 -> 32x32
        model.add(layers.Conv2DTranspose(64, (5, 5), strides=(2, 2), padding='same', use_bias=False))
        model.add(layers.BatchNormalization())
        model.add(layers.LeakyReLU(0.2))

        # Bloque 4: 32x32 -> 64x64
        model.add(layers.Conv2DTranspose(3, (5, 5), strides=(2, 2), padding='same', use_bias=False, activation='tanh'))

        return model

    def build_discriminator(self):

        # Discriminador: Clasifica imágenes como reales o falsas.

        model = tf.keras.Sequential(name='Discriminator')

        # Bloque 1: 64x64 -> 32x32
        model.add(layers.Conv2D(64, (5, 5), strides=(2, 2), padding='same', input_shape=self.img_shape))
        model.add(layers.LeakyReLU(0.2))
        model.add(layers.Dropout(0.3))

        # Bloque 2: 32x32 -> 16x16
        model.add(layers.Conv2D(128, (5, 5), strides=(2, 2), padding='same'))
        model.add(layers.LeakyReLU(0.2))
        model.add(layers.Dropout(0.3))

        # Bloque 3: 16x16 -> 8x8
        model.add(layers.Conv2D(256, (5, 5), strides=(2, 2), padding='same'))
        model.add(layers.LeakyReLU(0.2))
        model.add(layers.Dropout(0.3))

        # Bloque 4: 8x8 -> 4x4
        model.add(layers.Conv2D(512, (5, 5), strides=(2, 2), padding='same'))
        model.add(layers.LeakyReLU(0.2))
        model.add(layers.Dropout(0.3))

        # Clasificación
        model.add(layers.Flatten())
        model.add(layers.Dense(1))

        return model

    def discriminator_loss(self, real_output, fake_output):
        # Pérdida del discriminador.
        real_loss = self.cross_entropy(tf.ones_like(real_output), real_output)
        fake_loss = self.cross_entropy(tf.zeros_like(fake_output), fake_output)
        return real_loss + fake_loss

    def generator_loss(self, fake_output):
        # Pérdida del generador.
        return self.cross_entropy(tf.ones_like(fake_output), fake_output)

    @tf.function
    def train_step(self, real_images):
        # Un paso de entrenamiento.
        batch_size = tf.shape(real_images)[0]
        noise = tf.random.normal([batch_size, self.latent_dim])

        with tf.GradientTape() as gen_tape, tf.GradientTape() as disc_tape:
            # Generar imágenes falsas
            generated_images = self.generator(noise, training=True)

            # Obtener predicciones del discriminador
            real_output = self.discriminator(real_images, training=True)
            fake_output = self.discriminator(generated_images, training=True)

            # Calcular pérdidas
            gen_loss = self.generator_loss(fake_output)
            disc_loss = self.discriminator_loss(real_output, fake_output)

        # Calcular gradientes
        gen_gradients = gen_tape.gradient(gen_loss, self.generator.trainable_variables)
        disc_gradients = disc_tape.gradient(disc_loss, self.discriminator.trainable_variables)

        # Aplicar gradientes
        self.gen_optimizer.apply_gradients(zip(gen_gradients, self.generator.trainable_variables))
        self.disc_optimizer.apply_gradients(zip(disc_gradients, self.discriminator.trainable_variables))

        return gen_loss, disc_loss

    def generate_images(self, num_images=16, seed=None):
        # Genera imágenes usando el generador.
        if seed is not None:
            tf.random.set_seed(seed)
        noise = tf.random.normal([num_images, self.latent_dim])
        generated = self.generator(noise, training=False)
        return generated.numpy()

