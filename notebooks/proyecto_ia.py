class GANCallback:
    # Callback para monitorear el entrenamiento de la GAN.

    def __init__(self, gan, save_path, save_interval=10):
        self.gan = gan
        self.save_path = save_path
        self.save_interval = save_interval

        # Historial de métricas
        self.history = {
            'gen_loss': [],
            'disc_loss': [],
            'epochs': []
        }

        # Seed fijo para visualización consistente
        self.fixed_seed = tf.random.normal([16, gan.latent_dim])

    def on_epoch_end(self, epoch, gen_loss, disc_loss):
        # Se ejecuta al final de cada época.
        # Guardar métricas
        self.history['epochs'].append(epoch)
        self.history['gen_loss'].append(float(gen_loss))
        self.history['disc_loss'].append(float(disc_loss))

        # Guardar imágenes cada N épocas
        if (epoch + 1) % self.save_interval == 0:
            self.save_generated_images(epoch)
            self.save_metrics_plot(epoch)

    def save_generated_images(self, epoch):
        # Guarda una grilla de imágenes generadas.
        # Generar imágenes con seed fijo
        generated = self.gan.generator(self.fixed_seed, training=False)
        generated = (generated + 1) / 2

        fig, axes = plt.subplots(4, 4, figsize=(10, 10))

        for i, ax in enumerate(axes.flatten()):
            ax.imshow(generated[i])
            ax.axis('off')

        plt.suptitle(f'Imágenes Generadas - Época {epoch + 1}', fontsize=14)
        plt.tight_layout()

        save_path = os.path.join(self.save_path, f'generated_epoch_{epoch + 1:04d}.png')
        plt.savefig(save_path, dpi=150)
        plt.close()

    def save_metrics_plot(self, epoch):
        # Guarda gráfico de métricas de entrenamiento.
        fig, axes = plt.subplots(1, 2, figsize=(14, 5))

        # Pérdida del Generador
        axes[0].plot(self.history['epochs'], self.history['gen_loss'],
                     'b-', label='Generator Loss', linewidth=2)
        axes[0].set_xlabel('Época')
        axes[0].set_ylabel('Pérdida')
        axes[0].set_title('Pérdida del Generador')
        axes[0].legend()
        axes[0].grid(True, alpha=0.3)

        # Pérdida del Discriminador
        axes[1].plot(self.history['epochs'], self.history['disc_loss'],
                     'r-', label='Discriminator Loss', linewidth=2)
        axes[1].set_xlabel('Época')
        axes[1].set_ylabel('Pérdida')
        axes[1].set_title('Pérdida del Discriminador')
        axes[1].legend()
        axes[1].grid(True, alpha=0.3)

        plt.suptitle(f'Métricas de Entrenamiento - Época {epoch + 1}', fontsize=14)
        plt.tight_layout()

        save_path = os.path.join(self.save_path, 'training_metrics.png')
        plt.savefig(save_path, dpi=150)
        plt.close()

    def save_history(self):
        # Guarda el historial de entrenamiento en JSON.
        history_path = os.path.join(self.save_path, 'training_history.json')
        with open(history_path, 'w') as f:
            json.dump(self.history, f, indent=2)
        print(f" Historial guardado en: {history_path}")

