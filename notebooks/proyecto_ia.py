def preprocess_images(image_paths, target_size=(IMG_SIZE, IMG_SIZE)):

    images = []
    failed = 0

    for path in tqdm(image_paths, desc="Procesando imágenes"):
        try:
            # Cargar imagen
            img = load_img(path, target_size=target_size)
            img_array = img_to_array(img)

            # Si es escala de grises, convertir a RGB
            if img_array.shape[-1] == 1:
                img_array = np.concatenate([img_array] * 3, axis=-1)

            # Normalizar a [-1, 1]
            img_array = (img_array - 127.5) / 127.5
            images.append(img_array)

        except Exception as e:
            failed += 1
            continue

    print(f"\nImágenes procesadas: {len(images)}")
    print(f"Imágenes fallidas: {failed}")

    return np.array(images)

