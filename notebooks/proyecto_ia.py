def chat_with_agent(user_input: str, show_trace: bool = True):
    print("\n" + "=" * 60)
    print(f"Usuario: {user_input}")
    print("=" * 60)

    try:
        response = agent_executor.invoke({
            "input": user_input,
            "chat_history": []
        })

        agent_response = response['output']

        intermediate_steps = response.get("intermediate_steps", [])

        reasoning_logs = []
        tool_calls = []
        tool_results = []

        for step in intermediate_steps:
          action, result = step
          reasoning_logs.append(action.log)
          tool_calls.append(action.tool)
          tool_results.append(result)

        # Guardar trazabilidad completa
        logger.log_interaction(
            user_input=user_input,
            agent_response=agent_response,
            tools_used=tool_calls,
            reasoning=reasoning_logs,
            tool_outputs=tool_results
        )

        print("\n" + "-" * 60)
        print(f"Agente:\n{agent_response}")
        print("-" * 60)

        return agent_response

    except Exception as e:
        error_msg = f"Error: {str(e)}"
        print(f"\n {error_msg}")
        return error_msg


