# TOOL 4: Comparar Imágenes
@tool
def comparar_imagenes(num_imagenes: int = 4) -> str:
    """
    Genera múltiples imágenes para observar variabilidad de la GAN.
    """
    global STATE_SESSION_HISTORY

    num_imagenes = max(2, min(9, num_imagenes))
    images, seeds = [], []

    for _ in range(num_imagenes):
        seed = np.random.randint(0, 10000)
        seeds.append(seed)
        tf.random.set_seed(seed)
        noise = tf.random.normal([1, LATENT_DIM])
        generated = GAN_GENERATOR(noise, training=False)
        images.append((generated[0].numpy() + 1) / 2)

    cols = min(3, num_imagenes)
    rows = (num_imagenes + cols - 1) // cols
    fig, axes = plt.subplots(rows, cols, figsize=(4*cols, 4*rows))
    axes = axes.flatten()

    for i, ax in enumerate(axes):
        if i < len(images):
            ax.imshow(images[i])
            ax.set_title(f'MRI {i+1} (seed={seeds[i]})')
        ax.axis('off')

    timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
    save_path = os.path.join(REPORTS_PATH, f'comparacion_{timestamp}.png')
    plt.savefig(save_path, dpi=150)
    plt.show()

    STATE_SESSION_HISTORY.append({
        'accion': 'comparar_imagenes',
        'timestamp': timestamp,
        'seeds': seeds,
        'path': save_path
    })

    return f"Comparación de imágenes generada y guardada en {save_path}"


