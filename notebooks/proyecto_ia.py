import time
import json

class AgentLogger:

    def __init__(self):
        self.logs = []

    def log_interaction(self, user_input, agent_response, tools_used, reasoning, tool_outputs):
        entry = {
            "timestamp": time.strftime("%Y-%m-%d %H:%M:%S"),
            "user_input": user_input,
            "agent_response": agent_response,
            "tools_used": tools_used,
            "reasoning_steps": reasoning,
            "tool_outputs": tool_outputs
        }
        self.logs.append(entry)

    def show_summary(self):
        print("\n" + "=" * 60)
        print("RESUMEN DE TRAZABILIDAD DEL AGENTE")
        print("=" * 60)

        if not self.logs:
            print("⚠ No hay interacciones registradas.")
            return

        for i, entry in enumerate(self.logs, 1):
            print(f"\n🔹 Interacción #{i}")
            print(f"{entry['timestamp']}")
            print(f"Usuario: {entry['user_input']}")
            print(f"Respuesta del agente: {entry['agent_response']}")

            print("\nRazonamiento:")
            for r in entry["reasoning_steps"]:
                print(f"   - {r}")

            print("\n🛠 Herramientas usadas:")
            for t in entry["tools_used"]:
                print(f"   - {t}")

            print("\nResultados de herramientas:")
            for o in entry["tool_outputs"]:
                print(f"   - {str(o)[:120]}...")

    def save_to_json(self, filename="agent_trace.json"):
        with open(filename, "w", encoding="utf-8") as f:
            json.dump(self.logs, f, indent=4, ensure_ascii=False)
        print(f"\nArchivo de trazabilidad guardado en: {filename}")


# Definir el logger aquí
logger = AgentLogger()

